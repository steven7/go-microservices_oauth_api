func TestLoginUserNoError(t *testing.T) {
	rest.FlushMockups()
	rest.AddMockups(&rest.Mock{
		HTTPMethod:   http.MethodPost,
		URL:          "https://api.bookstore.com/users/login",
		ReqBody:      `{"email":"email@gmail.com","password":"the-password"}`,
		RespHTTPCode: http.StatusNotFound,
		RespBody:     `{"id": "888888888"", "first_name": "lolol", "last_name": "lolol", "email": "loltesttwo@email.com"}`,
	})

	repository := usersRepository{}

	user, err := repository.LoginUser("email@gmail.com", "the-password")

	assert.Nil(t, user)
	assert.NotNil(t, err)
	assert.EqualValues(t, 22, user.Id)
	assert.EqualValues(t, "john kk", user.FirstName)
	assert.EqualValues(t, "doelol", user.LastName)
	assert.EqualValues(t, "loltesttwo@email.com", user.Email)
}